int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	else if (c >= 0000 && c <= 0177)
		return (1);
	else if (c >= 0x00 && c <= 0x7f)
		return (1);
	return (0);
}
