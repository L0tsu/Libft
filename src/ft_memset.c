#include "libft.h"

void	*ft_memset(void *b, int c, t_size len)
{
	unsigned char	*p;
	t_size			i;

	i = 0;
	p = b;
	while (i < len)
	{
		p[i] = c;
		i++;
	}
	return (b);
}
