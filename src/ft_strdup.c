#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dup;

	i = 0;
	if (!*src)
		return (0);
	dup = (char *)malloc(sizeof(char) * ft_strlen(src));
	if (!dup)
		return (dup);
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
