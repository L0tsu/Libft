#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, t_size n)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && i < n && s1[i] && s2[i])
		i++;
	return (s1[i] - s2[i]);
}